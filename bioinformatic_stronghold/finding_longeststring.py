def find_lcs(fasta_data):
    lines = fasta_data.strip().split('\n')
    sequences = []
    current_seq = ""
    
    for line in lines:
        if line.startswith(">"):
            if current_seq:
                sequences.append(current_seq)
            current_seq = ""
        else:
            current_seq += line.strip()
    if current_seq:
        sequences.append(current_seq)

    if not sequences:
        return ""

    sequences.sort(key=len)
    shortest = sequences[0]
    others = sequences[1:]
    
    low = 1
    high = len(shortest)
    best_motif = ""
    
    while low <= high:
        mid = (low + high) // 2
        found_motif = None
        
        for i in range(len(shortest) - mid + 1):
            motif = shortest[i:i+mid]
            
            if all(motif in s for s in others):
                found_motif = motif
                break
        
        if found_motif:
            best_motif = found_motif
            low = mid + 1 
        else:
            high = mid - 1 
            
    return best_motif


