def solve_consensus_and_profile(fasta_data):

    lines = fasta_data.strip().split('\n')
    sequences = []
    current_seq = ""
    
    for line in lines:
        if line.startswith(">"):
            if current_seq:
                sequences.append(current_seq)
            current_seq = ""
        else:
            current_seq += line
    if current_seq:
        sequences.append(current_seq)
    if not sequences:
        return
        
    n = len(sequences[0])
    
    profile = {
        'A': [0] * n,
        'C': [0] * n,
        'G': [0] * n,
        'T': [0] * n
    }
    
    for seq in sequences:
        for i, char in enumerate(seq):
            if char in profile:
                profile[char][i] += 1
            
    consensus = []
    for i in range(n):
        max_count = -1
        max_char = ""
        for char in ['A', 'C', 'G', 'T']:
            if profile[char][i] > max_count:
                max_count = profile[char][i]
                max_char = char
        consensus.append(max_char)
        
    print("".join(consensus))
    for char in ['A', 'C', 'G', 'T']:
        counts = " ".join(map(str, profile[char]))
        print(f"{char}: {counts}")
