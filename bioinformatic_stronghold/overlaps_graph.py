def solve_overlap_graph(fasta_data, k=3):
    
    lines = [line.strip() for line in fasta_data.strip().split('\n') if line.strip()]
    dna_dict = {}
    current_id = ""
    
    for line in lines:
        if line.startswith(">"):
            current_id = line[1:]
            dna_dict[current_id] = ""
        else:
            dna_dict[current_id] += line
            
    ids = list(dna_dict.keys())
    
    for id_s in ids:
        for id_t in ids:
            if id_s == id_t:
                continue
            
            seq_s = dna_dict[id_s]
            seq_t = dna_dict[id_t]
            
            suffix_s = seq_s[-k:]
            prefix_t = seq_t[:k]
            
            if suffix_s == prefix_t:
                print(f"{id_s} {id_t}")

