
def gc_content(dna_string):
    if not dna_string:
        return 0
    gc_amount = dna_string.count('G') + dna_string.count('C')
    return (gc_amount / len(dna_string)) * 100


def highest_gc(fasta_data):
    lines = fasta_data.strip().split('\n')
    dna_dict = {}
    current_id = ""

    for line in lines:
        if line.startswith(">"):
            current_id = line[1:]
            dna_dict[current_id] = ""
        else:
            dna_dict[current_id] += line.strip()
    
    highest_id = ""
    highest_gc = -1.0

    for label, sequence in dna_dict.items():
        gc_val = gc_content(sequence)
        if gc_val > highest_gc:
            highest_gc = gc_val
            highest_id = label

    return highest_id, highest_gc

